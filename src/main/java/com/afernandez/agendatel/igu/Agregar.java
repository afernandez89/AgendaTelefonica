    public static boolean validarNumeros(String telefono){
        return telefono.matches("[0-9]*");
    }
    
    private void btnAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnadirActionPerformed
        
        if (txtNombre.getText().equals("") == true){
            mensaje("Ingrese un valor en el campo nombre","Error","Error en nombre");
        } 
        else if ((txtTelefono.getText().equals("") == true) || (!validarNumeros(txtTelefono.getText().trim())) ){
            mensaje("Ingrese un valor correcto en telefono","Error","Error en telefono");
        }
        else{
        String nombre = txtNombre.getText();
        int telefono = Integer.parseInt(txtTelefono.getText());
        String email = txtEmail.getText();

        
        control.anadirContacto(nombre, telefono, email);
        
        mensaje("Contacto AÃ±adido correctamente","Info","Agregado");
        
        txtNombre.setText("");
        txtTelefono.setText("");
        txtEmail.setText("");
        }
    }//GEN-LAST:event_btnAnadirActionPerformed

